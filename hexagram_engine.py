def _build_lookup() -> dict[int, Hexagram]:
    """构建 0-63 → Hexagram 的查找表"""
    lookup = {}
    for lower, upper, name, meaning, interpretation, bullish in _HEXAGRAM_DATA:
        h = _make_hexagram(lower, upper, name, meaning, interpretation, bullish)
        # 去重：山地剥 出现在两个位置（艮坤 和 艮坤实际上是同一个），取第一个
        if h.index not in lookup:
            lookup[h.index] = h
    return lookup

_HEXAGRAM_LOOKUP: dict[int, Hexagram] = _build_lookup()


# ============================================================
# 公开 API
# ============================================================

def get_hexagram(yao_bits: int) -> Hexagram:
    """
    根据 6-bit 爻位查找卦象

    Args:
        yao_bits: 6-bit 整数，bit0=初爻(最旧), bit5=上爻(最新)
                  阳爻=1, 阴爻=0

    Returns:
        Hexagram 对象

    Raises:
        ValueError: 如果 yao_bits 超出 0-63 范围
    """
    if not (0 <= yao_bits <= 63):
        raise ValueError(f"yao_bits 必须在 0-63 范围内，收到: {yao_bits}")

    if yao_bits not in _HEXAGRAM_LOOKUP:
        raise ValueError(f"未找到卦象: {yao_bits}")

    return _HEXAGRAM_LOOKUP[yao_bits]


def get_all_hexagrams() -> list[Hexagram]:
    """获取所有 64 卦"""
    return sorted(_HEXAGRAM_LOOKUP.values(), key=lambda h: h.index)


def calc_bullish_ratio(hexagrams: list[Hexagram]) -> float:
    """计算看涨卦象比例 (bullish_level >= 4)"""
    if not hexagrams:
        return 0.0
    bullish_count = sum(1 for h in hexagrams if h.bullish_level >= 4)
    return bullish_count / len(hexagrams)
