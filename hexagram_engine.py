"""
六十四卦完整数据库 — 包含卦名、卦象符号、卦义、行情解读
编码规则: 6-bit 整数, bit0=初爻, bit5=上爻, 阳爻=1 阴爻=0
K线映射: 最旧K线→上爻, 最新K线→初爻 (反序映射)
"""

from dataclasses import dataclass


@dataclass(frozen=True)
class Hexagram:
    """一个卦象的完整信息"""
    index: int         # 0-63 唯一编号
    name: str          # 卦名，如 "乾为天"
    symbol: str        # 卦象符号，如 "☰☰"
    meaning: str       # 卦义，如 "纯阳创生，六龙御天"
    interpretation: str  # 行情解读
    bullish_level: int  # 看涨程度 1(最空) ~ 5(最多)

    @property
    def yao_display(self) -> str:
        """爻的文本展示（从下到上）"""
        lines = []
        for i in range(6):
            if (self.index >> i) & 1:
                lines.append("———")
            else:
                lines.append("— —")
        return "\n".join(lines)


# ============================================================
# 八卦基础定义（从下到上的3位二进制）
# ============================================================

# 八卦: (中文名, 符号, bit2 bit1 bit0 从下到上)
_TRIGRAMS = {
    "乾": ("☰", (1, 1, 1)),  # 阳阳阳
    "兑": ("☱", (1, 1, 0)),  # 阳阳阴
    "离": ("☲", (1, 0, 1)),  # 阳阴阳
    "震": ("☳", (1, 0, 0)),  # 阳阴阴
    "巽": ("☴", (0, 1, 1)),  # 阴阳阳
    "坎": ("☵", (0, 1, 0)),  # 阴阳阴
    "艮": ("☶", (0, 0, 1)),  # 阴阴阳
    "坤": ("☷", (0, 0, 0)),  # 阴阴阴
}


def _trigram_bits(name: str) -> int:
    """八卦名 → 3-bit 值 (bit0=初爻)"""
    pattern = _TRIGRAMS[name][1]
    return pattern[0] | (pattern[1] << 1) | (pattern[2] << 2)


def _make_hexagram(lower: str, upper: str, name: str,
                   meaning: str, interpretation: str,
                   bullish_level: int) -> Hexagram:
    """构造卦象对象"""
    lower_bits = _trigram_bits(lower)
    upper_bits = _trigram_bits(upper)
    index = lower_bits | (upper_bits << 3)
    symbol = _TRIGRAMS[lower][0] + _TRIGRAMS[upper][0]
    return Hexagram(
        index=index,
        name=name,
        symbol=symbol,
        meaning=meaning,
        interpretation=interpretation,
        bullish_level=bullish_level,
    )


